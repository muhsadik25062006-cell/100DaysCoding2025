package coding100days;
public class harike58 {
    public static void main(String[] args) {     
        int N = 10;      
        System.out.print(" Angka dari 1 ke N : ");
        for (int i = 1; i <= N; i++) {
            System.out.print(i + " ");
        }      
        System.out.println();      
        System.out.print(" Angka dari N ke 1 : ");
        for (int i = N; i >= 1; i--) {
            System.out.print(i + " ");
        }
        System.out.println(" ");
    }
}

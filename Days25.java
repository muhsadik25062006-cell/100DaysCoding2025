package Isc;
public class Day25 {
    public static void main(String[] args) {
        //Increment
        int x = 5;
        x++; 
        System.out.println(" Nilai x = " + x);
        // pre-increment
        int a = 5;
        int b = ++x; 
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        
        //Decrement
        int y = 10;
        y--; // 
        System.out.println(" Nilai y = " + y);
        // pre-decrement
        int c = 5;
        int d = --x; 
        System.out.println("x = " + x);
        System.out.println("y = " + y);

    }
}

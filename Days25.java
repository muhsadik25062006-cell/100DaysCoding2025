package Isc;
public class Day25 {
    public static void main(String[] args) {
        //Increment
        int x = 5;
        x++; // sama dengan x = x + 1
        System.out.println(" Nilai x = " + x);
        
        //Decrement
        int y = 10;
        y--; // sama dengan y = y - 1
        System.out.println(" Nilai y = " + y);
    }
}
